CREATE SCHEMA Other;

CREATE TABLE Other.SchemaOther 
(
	Id SERIAL NOT NULL PRIMARY KEY,
	Name TEXT NOT NULL
);

CREATE TABLE CompositeKeys
(
	Key1 INT NOT NULL,
	Key2 INT NOT NULL,
	PRIMARY KEY (Key1, Key2)
);

CREATE TABLE KeyAlias
(
	Key SERIAL NOT NULL PRIMARY KEY,
	Name TEXT NOT NULL
);

CREATE TABLE KeyInt32
(
	Id SERIAL NOT NULL PRIMARY KEY,
	Name TEXT NOT NULL
);

CREATE TABLE KeyInt64
(
	Id BIGSERIAL NOT NULL PRIMARY KEY,
	Name TEXT NOT NULL
);

CREATE TABLE KeyString
(
	Name TEXT PRIMARY KEY,
	Age INT NOT NULL
);

CREATE TABLE NoAutoIdentity
(
	Id INT NOT NULL PRIMARY KEY,
	Name TEXT NOT NULL
);

CREATE TABLE NoKey
(
	Name TEXT NOT NULL,
	Age INT NOT NULL
);

CREATE TABLE PropertyEnum
(
	Id INT NOT NULL PRIMARY KEY,
	FavoriteColor INT NULL
);

CREATE TABLE Users
(
	Id SERIAL NOT NULL PRIMARY KEY,
	Name TEXT NOT NULL,
	Age INT NOT NULL
);